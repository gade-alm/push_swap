#include "ft_printf.h"

int	ft_ptr(t_llu n)
{
	unsigned int				i;

	if (n >= 16)
		ft_ptr(n / 16);
	write (1, &"0123456789abcdef"[n % 16], 1);
	i = 1;
	if (n >= 16)
	{
		while (n > 15)
		{
			n /= 16;
			i++;
		}
	}
	return (i);
}